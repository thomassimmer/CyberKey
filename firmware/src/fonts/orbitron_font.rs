pub fn draw_text_prop<D>(
    display: &mut D,
    text: &str,
    position: Point,
    color: Rgb565,
) -> Result<Point, D::Error>
where
    D: DrawTarget<Color = Rgb565>,
{
    let mut cursor = position;

    for c in text.chars() {
        let code = c as u32;
        if !(32..=126).contains(&code) {
            continue;
        }

        let (offset, w, h, x, y) = GLYPH_INFO[(code - 32) as usize];
        if w == 0 {
            cursor.x += 5; // Espace par défaut
            continue;
        }

        let bytes_per_row = (w as usize).div_ceil(8);
        let glyph_data =
            &FONT_DATA[offset as usize..(offset as usize + bytes_per_row * h as usize)];

        // Calcul de la position Y pour l'alignement sur la baseline
        // baseline = 17. Si y=-4 et h=15, top = 17 - (15-4) = 6.
        let top = cursor.y + (BASELINE - (h as i32 + y as i32));
        let left = cursor.x + x as i32;

        for row in 0..h as i32 {
            for col in 0..w as i32 {
                let byte_idx = (col as usize) / 8;
                let bit_idx = 7 - (col % 8);
                let byte = glyph_data[row as usize * bytes_per_row + byte_idx];

                if (byte & (1 << bit_idx)) != 0 {
                    display.draw_iter(core::iter::once(Pixel(
                        Point::new(left + col, top + row),
                        color,
                    )))?;
                }
            }
        }
        cursor.x += w as i32 + x as i32 + 1; // +1 pour l'espacement entre lettres
    }
    Ok(cursor)
}

pub fn get_text_width(text: &str) -> i32 {
    let mut width = 0;
    for c in text.chars() {
        let code = c as u32;
        if !(32..=126).contains(&code) {
            continue;
        }
        let (_, w, _, x, _) = GLYPH_INFO[(code - 32) as usize];
        if w == 0 {
            width += 5;
        } else {
            width += w as i32 + x as i32 + 1;
        }
    }
    if width > 0 {
        width - 1
    } else {
        0
    } // On retire le dernier +1
}
